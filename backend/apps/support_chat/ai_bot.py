"""Бот поддержки на простых правилах (без внешнего ИИ-провайдера — решение
согласовано с пользователем: быстро и без затрат, настоящий ИИ можно
подключить позже, заменив только reply())."""

RULES = [
    (('тип', 'typing', 'auto', 'gradual'), (
        'В ATOM используется гибридная типизация: без явного типа (`make x = 1;`) '
        'переменная — `auto`, проверка типов уходит в рантайм. С явным типом '
        '(`make x -> Int = 1;`) проверка строгая, на этапе компиляции.'
    )),
    (('импорт', 'use "', 'модул'), (
        'Встроенные модули подключаются как `use "@io";` / `@math` / `@array`. '
        'Свой файл — `use "./path/to/file.atm";`, путь относительно проекта.'
    )),
    (('class', 'наслед', 'extends'), (
        'Наследование — `class Child extends Parent { new() { } }`. Конструктор — '
        'метод `new()`; явного `super()` для вызова родительского конструктора пока нет.'
    )),
    (('ошибка', 'не работает', 'баг', 'падает', 'crash'), (
        'Похоже на баг. Опишите, пожалуйста, что именно выводит компилятор — если '
        'не разберёмся здесь, можно завести обращение в профиле (вкладка «Мои баги»).'
    )),
    (('docker', 'установ', 'запуск', 'install', 'setup'), (
        'Подробная установка через Docker описана в SETUP.md и README.md в корне репозитория.'
    )),
    (('git', 'коммит', 'github'), (
        'Закоммитить проект на GitHub можно из редактора — авторизуйтесь через GitHub '
        '(нужен scope repo) и используйте кнопку публикации в профиле/редакторе.'
    )),
]

FALLBACK = (
    'Пока не нашёл готового ответа на это. Можете уточнить вопрос или подключить '
    'живого разработчика — кнопка ниже.'
)


def reply(text):
    lowered = text.lower()
    for keywords, answer in RULES:
        if any(k in lowered for k in keywords):
            return answer
    return FALLBACK
